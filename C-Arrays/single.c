#include <stdio.h>

int main()
{
    // Declare and initialize an int array
    int numArray[] = {0, 1, 2, 3, 4, 5};

    // Access an element in array
    printf("%d\n", numArray[4]);

    // Declare and initialize a char array
    char charArray[] = "hello";
    printf("%c\n", charArray[4]);

    return 0;
}